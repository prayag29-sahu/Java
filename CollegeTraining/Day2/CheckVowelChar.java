public class CheckVowelChar {
    public static void main(String[] args) {
        char ch = 'w';
        if(ch=='a'||ch=='e'||ch=='i'||ch=='o'||ch=='u'||ch=='A'||ch=='E'||ch=='I'||ch=='O'||ch=='U')
        {
            System.out.println(ch+" is Vowel char.");
        } else 
        {
            System.out.println(ch+" is not Vowel char.");
        }
    }
}
