public class Operators {
    public static void main(String[] args) {
        System.out.println(5<6);// Relational ope   o/p (true)
        boolean a = true;
        boolean b = false;
        System.out.println(5<6);// Relational ope   o/p (true)
        System.out.println(a&&b);// logical AND ope   o/p (false)
        System.out.println(a||b);// logical OR ope   o/p (true)
        int p = 5;
        int q = 4;
        System.out.println(p&q);// Bitwise OR ope   o/p (4) which is 101 & 100 = 100
    }
    
}
