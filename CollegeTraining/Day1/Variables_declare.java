public class Variables_declare {
    public static void main(String[] args) {
        byte b = 0;
        short s = 10;
        int i = 100;
        long l = 10000l;
        float f = 5.5f;
        double d = 1000000;
        char ch = 'A';
        boolean bool = false;
        System.out.println(b+" "+s+" "+i+" "+l+" "+f+" "+d+" "+ch+" "+bool);

    }
}
