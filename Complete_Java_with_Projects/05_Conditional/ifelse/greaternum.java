public class greaternum {
    public static void main(String[] args) {
        int a = 15;
        int b = 10;

        if (a > b) {
            System.out.println("a is greater than b");
        } else if (a < b) {
            System.out.println("a is less than b");
        } else {
            System.out.println("a is equal to b");
        }
        // a is greater than b
    }
}
