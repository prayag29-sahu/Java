class Strings {
    public static void main(String[] args) {

        int age = 19;
        String name = "John";
        System.out.println(name);  // Hello
        System.out.println("Hello "+name);  // Hello John 
        System.out.println("Hello "+name+" "+age);  // Hello John 19
        System.out.println("Hello "+name+" you are "+age+" Years old");  // Hello John you are 19 Years old

    }
}