public class Char {
    public static void main(String[] args) {
        
        char ch1 = 'A';
        System.out.println(ch1); // A
        
        char ch2 = '!';
        System.out.println(ch2); // !
        
        char ch3 = '$';
        System.out.println(ch3); // $
        
        char ch4 = '4';
        System.out.println(ch4); // 4
    }
}
