
public class Double {

    public static void main(String[] args) {

        double d1 = 10;
        System.out.println(d1); // 10.0

        double d2 = 10.56;
        System.out.println(d2); // 10.56

        double d3 = -10.34;
        System.out.println(d3); // -10.34

        double d4 = -10.3436473265745642352;
        System.out.println(d4); // 10.343647326574564    // only 15 digit in decimal
    }
}
